""" tipos de dados padrão do Python são:
    Inteiro (int)
    Ponto Flutuante ou Decimal (float)
    Tipo Complexo (complex)
    String (str)
    Boolean (bool)
    List (list)
    Tuple
    Dictionary (dic)
"""
##String(texto)
nome = "Ricardo Ferreira"

# int (inteiro)
idade = 55

#boolean(verdadeiro e falso)
aprovado = True
aprovado = False

#float(double) tipo de dado para trabalhar com números decimais e negativos
altura = 1.70

#list, muito comum trabalhar com vetor
# estrutura de dados mutavel e ordenada que pode contar
#diversos tipos dentro de si
frutas = ["maça", "banana", "alface"]

#tuple
#estrutura de dados imutavel (não altera após criar)
cores = ("vermelho", "verde", "azul")

#dict
#estrutura de dados mutavel que pode ser alterada
aluno = {
    "nome": "Ricardo Ferreira",
    "nota": 4.0
}

#set
#estrutura de dados mutavel, mas não ordenado e não indexada
#reordena a lista e armazena sem duplicar
conj_num = {1,2,2,3} #1,2,3


